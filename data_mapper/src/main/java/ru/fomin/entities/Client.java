package ru.fomin.entities;

public class Client {

    private Long id;
    private String name;
    private Integer age;

    public Long getId() {
        return id;
    }

    public String getNa() {
        return name;
    }

    public void setNa(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

}
